        col = [set() for i in range(9)]
        subboard = [set() for i in range(9)]

        # ä»ä¸Šåˆ°ä¸‹
        for i in range(9):
            # ä»å·¦åˆ°å³:
            for j in range(9):
                if board[i][j] != '.':
                    k = (i // 3) * 3 + j // 3

                    if board[i][j] not in row[i]:
                        row[i].add(board[i][j])
                    else:
                        return False

                    if board[i][j] not in col[j]:
                        col[j].add(board[i][j])
                    else:
                        return False

                    if board[i][j] not in subboard[k]:
                        subboard[k].add(board[i][j])
                    else:
                        return False
        return True
