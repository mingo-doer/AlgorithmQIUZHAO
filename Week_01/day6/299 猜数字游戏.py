def getHint( secret: str, guess: str) -> str:
    bulls , cows = 0 , 0
    for i in range(len(secret)):
        if secret[i] == guess[i] :
            bulls += 1
    d1 = dict(collections.Counter(list(secret)))
    d2 = dict(collections.Counter(list(guess)))

    for i in d1:
        if i in d2:
            cows += min(d1[i],d2[i])
    cows = cows - bulls
    return str(bulls) + "A" + str(cows) + "B"

